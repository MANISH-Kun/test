public class loopingStatements {
    public static void main(String[] args) {
        // while loop
        int i=0;
        while(i<=2){
            System.out.println("while loop executed:"+i+"time");
            i++;
        }
        // do while loop
        do {
            System.out.println("do-while loop executed"+i+"time");
            i++;
            
        } while (i<=4);

        // for loop 

        for(i=0;i<=2;i++){
            System.out.println("for loop executed :"+i+"th time");
        }
        

    

    }
}
